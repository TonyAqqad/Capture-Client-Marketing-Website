// Helper Functions

export function getIntegrationBySlug(slug: string): Integration | undefined {
  return integrations.find((integration) => integration.slug === slug);
}

export function getIntegrationsByCategory(category: string): Integration[] {
  return integrations.filter((integration) => integration.category === category);
}

export function getPopularIntegrations(): Integration[] {
  return integrations.filter((integration) => integration.popular === true);
}

export function getIntegrationsByIndustry(industry: string): Integration[] {
  return integrations.filter(
    (integration) =>
      integration.industries?.includes(industry) ||
      integration.industries?.includes('All Industries')
  );
}

export function searchIntegrations(query: string): Integration[] {
  const lowerQuery = query.toLowerCase();
  return integrations.filter(
    (integration) =>
      integration.name.toLowerCase().includes(lowerQuery) ||
      integration.description.toLowerCase().includes(lowerQuery) ||
      integration.shortDescription.toLowerCase().includes(lowerQuery) ||
      integration.keyFeatures.some((feature) =>
        feature.toLowerCase().includes(lowerQuery)
      )
  );
}

export function getCategoryById(categoryId: string): IntegrationCategory | undefined {
  return integrationCategories.find((cat) => cat.id === categoryId);
}

// Statistics
export function getIntegrationStats() {
  return {
    total: integrations.length,
    byCategory: integrationCategories.map((cat) => ({
      category: cat.name,
      count: getIntegrationsByCategory(cat.id).length,
    })),
    popular: getPopularIntegrations().length,
    categories: integrationCategories.length,
  };
}
